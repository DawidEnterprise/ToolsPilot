export function textToAsciiArt(text: string): string {
  const chars = text.toUpperCase().split("");
  const lines: string[] = ["", "", "", "", ""];
  for (const ch of chars) {
    const glyph = FONT[ch] || FONT[" "];
    for (let row = 0; row < 5; row++) {
      lines[row] += glyph[row] + "  ";
    }
  }
  return lines.join("\n");
}
